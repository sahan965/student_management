    private void btnupdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnupdateActionPerformed
        // TODO add your handling code here:
        try {
            Class.forName("com.mysql.jdbc.Driver");
        Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3307/project","root","");
        
        String upid = txtid.getText();
        String upname = txtname.getText();
        String upgade = txtgrade.getText();
        String upbirthday = txtbirthday.getText();
        String upaddress = txtaddress.getText();
        String upphone = txtphone.getText();
        
        String sql = "update student set name=?, grade=?, birthdate=?, address=?, parentPhone=? where id=?";
        
        PreparedStatement preparedStatement = con.prepareStatement(sql);
        preparedStatement.setString(1, upname);
        preparedStatement.setString(2, upgade);
        preparedStatement.setString(3, upbirthday);
        preparedStatement.setString(4, upaddress);
        preparedStatement.setString(5, upphone);
        preparedStatement.setString(6, upid);
        
        preparedStatement.executeUpdate();
        
        refreshTable();
        clear();
        
        JOptionPane.showMessageDialog(this, "Record updated successfully");
        } catch (Exception e) {
            System.out.print(e);
        }
    }//GEN-LAST:event_btnupdateActionPerformed

