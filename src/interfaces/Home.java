    public void refreshTable() {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0); // Clear existing rows

        // Fetch latest data from the database and add to the table model
        try {
            Class.forName("com.mysql.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3307/project", "root", "");

            Statement st = con.createStatement();

            String sql = "select * from student";
            ResultSet rs = st.executeQuery(sql);

            while (rs.next()) {
                String id = String.valueOf(rs.getInt("id"));
                String username = rs.getString("name");
                String grade = rs.getString("grade");
                String birthday = rs.getString("birthdate");
                String address = rs.getString("address");
                String parentphone = rs.getString("parentPhone");

                String tbData[] = {id, username, grade, birthday, address, parentphone};
                model.addRow(tbData);
            }

        } catch (Exception e) {
            System.out.println(e.getMessage());
        }
    }
    
